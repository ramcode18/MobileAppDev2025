void main() {
  // Implicitly declared variable (inferred type)
  var age = 25;
  var name = "John";

  // Explicitly declared variables
  int score = 100;
  String city = "New York";

  // Final variable (cannot be reassigned)
  final double pi = 3.14;

  // Constant variable (must be a compile-time constant)
  const int maxLimit = 50;

  // Printing the variables
  print('Age: $age');
  print('Name: $name');
  print('Score: $score');
  print('City: $city');
  print('Pi: $pi');
  print('Max Limit: $maxLimit');
}
