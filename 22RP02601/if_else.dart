void main() {
  int salary = 20000;
  if (salary >= 18000) {
    print('Adult');
  } else {
    print('Minor');
  }
}

/*
Explanation:
- `if-else` evaluates a condition and executes one block.
- `if` checks the condition; `else` is fallback.
Contrast:
- More readable for complex conditions than ternary.
*/

